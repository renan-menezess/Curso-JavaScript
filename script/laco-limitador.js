/* Objetivo: Escrever uma mensagem limitando-a em 10 vezes através de um contador que será
incrementado de mais 1
*/
/* Definicao das variaveis */
var js_contador=1;
/* Laço de Repetição Condicional - while */
while (js_contador <= 10){
    document.write('<p>' + js_contador + '&ordf; Repetição de uma única linha de comando dentro de um laço condicional: ');
    js_contador +=1;
}
   